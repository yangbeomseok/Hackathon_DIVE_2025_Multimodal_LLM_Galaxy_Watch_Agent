    fun calculateDistance(userLat: Double, userLon: Double, pointLat: Double, pointLon: Double): String {
        val distance = kotlin.math.sqrt(
            (userLat - pointLat) * (userLat - pointLat) + 
            (userLon - pointLon) * (userLon - pointLon)
        ) * 111.32 // 대략적인 km 변환
        
        return when {
            distance < 1.0 -> "${(distance * 1000).toInt()}m"
            else -> "${distance.toInt()}km"
        }
    }
